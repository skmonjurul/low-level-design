package com.skmonjurul.splitwise.model;

public abstract class Base {
    protected String id;
    protected String name;

    public Base(String id, String name) {
        this.id = id;
        this.name = name;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
